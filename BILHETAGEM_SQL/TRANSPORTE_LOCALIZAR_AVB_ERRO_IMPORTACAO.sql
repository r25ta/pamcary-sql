SELECT tsaa.*
FROM TRELAC_SITUA_AVERB_ATM tsaa 
INNER JOIN TAVERBACAO_IMPOR_ATM tia ON tia.CTL_ATM = tsaa.CTL_ATM 
INNER JOIN TSITUA_IMPOR_ATM tia2 ON tia2.SIT_IMPOR_ATM = tsaa.SIT_IMPOR_ATM 
WHERE tia.COD_CNPJ_SEG = '33767574000149'
AND tsaa.SIT_IMPOR_ATM NOT IN (0,26,28)
and tia.DAT_INCLU_ATM BETWEEN '2024-09-01' AND '2024-10-16'
