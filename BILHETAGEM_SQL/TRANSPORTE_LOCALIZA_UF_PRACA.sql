SELECT P.NUM_PAIS
,P.NOM_PAIS 
,UF.NUM_UNFED 
,UF.SIG_UNFED 
,PR.NUM_PRACA 
,PR.DES_PRACA 
,PR.COD_CEP 
FROM PAIS P INNER JOIN UNIDADE_FEDERAL UF
INNER JOIN PRACA PR ON PR.NUM_UNFED = UF.NUM_UNFED 
ON UF.NUM_PAIS = P.NUM_PAIS 
WHERE P.NUM_PAIS IN (999
,100
,101
,102
,103
,104
,105
,106
,107
,108
,109
,110
,111)
AND UF.NUM_UNFED NOT IN (0,35,88,99)
AND PR.STA_ATIVO = 'S'
ORDER BY P.SIG_PAIS, UF.SIG_UNFED, PR.DES_PRACA 

