
/*TOTALIZADOR DE ALERTAS*/
SELECT TAV.DES_ALERT_VIE, COUNT(1)
FROM LOG_ENVIO_ALERTA LEA, TIPO_ALERTA_VIAGEM TAV
WHERE LEA.DHR_ALTER BETWEEN TO_DATE('20190826000000','YYYYMMDDHH24MISS') AND TO_DATE('20190827235959','YYYYMMDDHH24MISS')
AND TAV.TIP_ALERT_VIE = LEA.TIP_ALERT_VIE
GROUP BY TAV.DES_ALERT_VIE

select TP.DES_OPERA_TIP, count(1) as total
from TIPO_OPERACAO TP
  , PLANO_VIAGEM HPV
where TP.CTL_OPERA_TIP = HPV.CTL_OPERA_TIP
and hpv.DHR_MANUT BETWEEN TO_DATE('20190801000000','YYYYMMDDHH24MISS') AND TO_DATE('20190831000000','YYYYMMDDHH24MISS')
group by TP.DES_OPERA_TIP

SELECT TAV.DES_ALERT_VIE, COUNT(1)
FROM LOG_ENVIO_ALERTA LEA, TIPO_ALERTA_VIAGEM TAV
WHERE LEA.DHR_ALTER BETWEEN TO_DATE('20190801000000','YYYYMMDDHH24MISS') AND TO_DATE('20190831235959','YYYYMMDDHH24MISS')
AND TAV.TIP_ALERT_VIE = LEA.TIP_ALERT_VIE
GROUP BY TAV.DES_ALERT_VIE


SELECT NOM_USER_INC, COUNT(1) AS TOTAL
FROM hist_PLANO_VIAGEM
WHERE DHR_MANUT BETWEEN TO_DATE('20180801000000','YYYYMMDDHH24MISS') AND TO_DATE('20180831000000','YYYYMMDDHH24MISS')
AND NOM_USER_INC not LIKE '%INTEGRACAO%'
GROUP BY NOM_USER_INC
