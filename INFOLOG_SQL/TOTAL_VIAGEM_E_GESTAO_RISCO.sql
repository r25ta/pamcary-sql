SELECT COUNT(1) AS QTDE_TOTAL_VIAGENS_INFOLOG
FROM PLANO_VIAGEM P
Where Dhr_Inclu Between To_Date('01/05/2020 00:00:00','DD/MM/YYYY HH24:MI:SS') 
AND TO_DATE('31/05/2020 23:59:59','DD/MM/YYYY HH24:MI:SS');


SELECT COUNT(1) AS QTDE, 
DECODE(I.NUM_SITUA_VAL, 1, 'VIAGENS APROVADAS', 2, 'VIAGENS APROVADAS COM RESTRIÇÕES', 3, 'VIAGENS REPROVADAS', 
5, 'VIAGENS APROVADAS COM AUTORIZACAO ESPECIAL') AS DESCRICAO
FROM PLANO_VIAGEM P, INF_SITUA_VALID_PLVIA I
WHERE Dhr_Inclu Between To_Date('01/05/2020 00:00:00','DD/MM/YYYY HH24:MI:SS') 
AND TO_DATE('31/05/2020 23:59:59','DD/MM/YYYY HH24:MI:SS')
AND P.NUM_SITUA_VAL = I.NUM_SITUA_VAL
AND I.NUM_SITUA_VAL IN (1,2,3,5)
GROUP BY I.NUM_SITUA_VAL;
