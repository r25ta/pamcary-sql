SELECT T.DES_OPERA_TIP AS CLIENTE, TO_CHAR(DHR_INCLU,'DD/MM/YYYY') AS DATA_CRIACAO, H.CTL_PLVIA AS NUMERO_VIAGEM,
(SELECT TRUNC(SUM(QTD_DISTA_TRE/1000),2) FROM DESTINATARIO_PLANO_VIAGEM WHERE CTL_PLVIA = H.CTL_PLVIA) AS KM_PREVISTO,
COALESCE((SELECT SUM(QTD_DISTA_RAL) FROM DESTINATARIO_PLANO_VIAGEM WHERE CTL_PLVIA = H.CTL_PLVIA), 0.00) AS KM_REALIZADO ,
(SELECT TRUNC((((SUM(QTD_DISTA_TRE/1000) / 2.7) * 2.606) / 1000 ),2) FROM DESTINATARIO_PLANO_VIAGEM
    WHERE CTL_PLVIA = H.CTL_PLVIA) AS PEGADA_CARBONO,
PRO.DES_PRACA AS CIDADE_ORIGEM, UFO.SIG_UNFED AS ESTADO_ORIGEM, PRD.DES_PRACA AS CIDADE_DESTINO_FINAL, UFD.SIG_UNFED AS ESTADO_DESTINO_FINAL,
VT.NOM_GUERR AS TRANSPORTADORA
FROM PLANO_VIAGEM H, TIPO_OPERACAO T,
ENDERECO_VINCULADO EVO, PRACA PRO, UNIDADE_FEDERAL UFO,
ENDERECO_VINCULADO EVD, PRACA PRD, UNIDADE_FEDERAL UFD, VINCULADO VT
WHERE H.CTL_OPERA_TIP = T.CTL_OPERA_TIP
AND H.DHR_INCLU BETWEEN TO_DATE('01/06/2024 00:00:00','DD/MM/YYYY HH24:MI:SS') AND TO_DATE('30/06/2024 23:59:59','DD/MM/YYYY HH24:MI:SS')
AND SIT_PLVIA NOT IN (3,4,5,6,7,10,14,15)
AND H.CTL_REMET = EVO.CTL_VINCD
AND EVO.COD_PRACA = PRO.COD_PRACA
AND PRO.COD_UNFED = UFO.COD_UNFED
AND H.CTL_DESTI = EVD.CTL_VINCD
AND EVD.COD_PRACA = PRD.COD_PRACA
AND PRD.COD_UNFED = UFD.COD_UNFED
AND H.CTL_TRNSP = VT.CTL_VINCD
ORDER BY H.CTL_PLVIA;